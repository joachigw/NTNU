public class fromInchesToCentimeters {

  public static void main(String[] args) {
    double inches = 5.08; // Deklarerer antall tommer
    double centimeters = inches / 2.54; // Omgjør tommer til centimeter ved å dele på 2.54
    System.out.println(
      inches + " tommer er det samme som " + centimeters + " centimeter."
    ); // Skriver ut svaret
  }
}
